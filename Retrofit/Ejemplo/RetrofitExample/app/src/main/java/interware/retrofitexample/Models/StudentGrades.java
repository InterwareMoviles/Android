public class StudentGrades {

    private String nombre, matematicas, español, biologia, fisica, quimica, historia, civica, edfisica;

    public StudentGrades(){}

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getMatematicas() {
        return matematicas;
    }

    public void setMatematicas(String matematicas) {
        this.matematicas = matematicas;
    }

    public String getEspañol() {
        return español;
    }

    public void setEspañol(String español) {
        this.español = español;
    }

    public String getBiologia() {
        return biologia;
    }

    public void setBiologia(String biologia) {
        this.biologia = biologia;
    }

    public String getFisica() {
        return fisica;
    }

    public void setFisica(String fisica) {
        this.fisica = fisica;
    }

    public String getQuimica() {
        return quimica;
    }

    public void setQuimica(String quimica) {
        this.quimica = quimica;
    }

    public String getHistoria() {
        return historia;
    }

    public void setHistoria(String historia) {
        this.historia = historia;
    }

    public String getCivica() {
        return civica;
    }

    public void setCivica(String civica) {
        this.civica = civica;
    }

    public String getEdfisica() {
        return edfisica;
    }

    public void setEdfisica(String edfisica) {
        this.edfisica = edfisica;
    }
}
